// Object literal
var o = {};
// Object constructor
var oc = new Object();

// Examples of literals and built-in constructors.

// Array literal
var a = [];
// Array constructor
var ac = new Array();

// Regular expression literal
var r = /javascript/g;
// Regular expression constructor
var rc = new RegExp('javascript', 'g');

// String, number, and boolean
var s = 'dog';
var sc = new String();

var n = 123;
var nc = new Number();

var b = false;
var bc = new Boolean();
